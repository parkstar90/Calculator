public class Calculator {

    /**
     * a + b 를 리턴한다.
     *
     * @param a
     * @param b
     * @return
     */
    public int sum(int a, int b){
        return a + b;
    }

    /**
     * a - b 를 리턴한다.
     *
     * @param a
     * @param b
     * @return
     */
    public int sub(int a, int b){
        return a - b;
    }
}
